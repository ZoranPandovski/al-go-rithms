s = "This is a Hacktoberfest String"

print('String in all lower case-',s.lower())  #lowercase

print('String in all upper case-', s.upper())  #uppercase

print('String is Capitalize-',s.capitalize())  #title_case

print('String is in Title case-',s.title())  #camel_case

print('Swapcase-',s.swapcase())  #Swap the given case
